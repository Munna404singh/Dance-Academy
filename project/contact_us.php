         	  	<h2 align="justify">Healthy Diets</h2>
        <p align="justify">How to feed is a common question among dancers. Non dancers usually also ask about the dancer diet, because they associate the beauty and health of some dancers’ bodies with what they eat.<br/><br/>
<b>I.</b>&nbsp;  Some how that association might be true but, as you may know, that healthy and efficient body needs to be cultivated through the reasonable application of both, a balanced diet and a healthy dance practice that suits each person.<br/><br/>
<b>II.</b>&nbsp;  There is no ‘magical dancer diet’ or ‘secret way of feeding’ among dancers. Instead, what constructs the so acclaimed ‘dancer’s beauty’ is a general healthy way of living, of which the dancer diet is a part.  You might be thinking about ballet, because many people admire the thinness of its dancers. <br/><br/>
<b>III.</b>&nbsp;  Healthy diets is not a rule, but trying to be so slim implies a high risk of acquiring feeding disordessrs. That is a real threat for the health of anyone, so it is very important that dancers understand that a deliberate diet should aim to give them health and efficiency over aesthetic goals.<br/><br/>
<b>IV.</b>&nbsp;	One important value of contemporary dance is the fact that it has an immense field of creative and expressive possibilities, which include and accept the human body in its multiple sides.<br/><br/>
<b>V.</b>&nbsp;	Therefore, the search for ideal, stereotyped or according-to-fashion bodies is not a rule and is even often rejected. So, I will be describing a dancer diet that focuses on the maintenance of health and an effective capability to perform. Yet in the long term, the beautiful bodies that people want to have, may appear as a secondary effect of its proper application.<br/><br/>
<b>VI.</b>&nbsp;	Feeding, together with breathing, is one of our two ways of acquiring the basic energy to be alive. But, the way in which that energy has an effect on us does not only depend on what we eat. It is also affected by the moment in which we eat and by the amounts that we ingest.
</p>
 
