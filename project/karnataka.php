               <h2> Yakshgana</h2>
<p align="justify"  style="color:#000;">Although their exist conflict about an origination of folk theatre form Yakshgana, it is agreed that it is an ancient art. Some believe that it emerged in the 16th century and by others in the 18th century. Its origins and growth can be traced back in Sanskrit literature in its theatre on the one hand, and even in Kannada literature and the many forms of ritual dancing and music prevalent in the area on the other. It relates with many of the traditions and conventions of the Sanskrit theatre or drama, particularly those of the Purvaranga, and the existence of a character, vidushak.The original form of Yakshgana involves the use of recitative modes of poetry sung in loud voice, melodies of music, rhythm, and dance techniques and above all, costuming and graceful make up. It is distinctly differs in many ways from the norms of the Sanskrit stage, as it does not contain a highly elaborate language of hand gestures and eye- gestures. But it should be noted that it is closely related to developments in literature in the adjoining states of Andhra Pradesh and Tamil Nadu and has some affinities to literary forms.Unfortunately, hardly anything of the literature from the period A.D. 450 to 850 has survived & accessible today. However, from the work of Kavirajamarga mainly a treatise on poetry, some valuable information can be gathered about literary styles, metrical forms etc; all of which have some relevance to the literary composition called the Yakshgana of a much later period is available.</p>

