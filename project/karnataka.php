<h2 align="justify">Bayalata</h2>

<p align="justify" style="color:#000;">Bayalata is a form of Yakshagana found in southern Indian region of Karnataka featuring stories of from Indian epic poetry and the Puranas rendered as dance and drama. Bayalāṭa literally means open theater drama and marks the end of harvest season. The most popular theme for bayalāṭa is the story of Kōṭi and Cennayya, which has deep-rooted significance for the people of Tulu Nadu.</p>

<h2 align="justify">Dollu Kunitha</h2>

<p align="justify" style="color:#000;">The Dollu Kunitha is a popular drum dance of Karnataka. The vigorous drum dance is performed by the men of the shepherd community known as 'Kurba'. Powerful drumming, acrobatic movements and attractive formations are the notable highlights of the dance. The men have large drums, decorated with colored cloth, slung from their necks, and they beat the drums as they dance with nimble foot and leg movement. The dance is at times accompanied by songs, which are religious or in praise of war.</p>

<h2 align="justify"> Veeragase</h2>

<p align="justify"style="color:#000;">Female artists performing Veeragase is a dance form prevalent in the state of Karnataka, India. It is a vigorous dance based on Hindu mythology and involves very intense energy-sapping dance movements. Veeragase is one of the dances demonstrated in the Dasara procession held in Mysore. This dance is performed during festivals and mainly in the Hindu months of Shravana and Karthika.</p>

