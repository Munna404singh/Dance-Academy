                 <p align="justify"style="color:#000;">Dance in India comprises the varied styles of dances in the country. As with other aspects of Indian culture, different forms of dances originated in different parts of India, developed according to the local traditions and also imbibed elements from other parts of the country. Sangeet Natak Akademi, the national academy for performing arts, recognizes eight distinctive traditional dances as Indian classical dances, which might have origin in religious activities of distant past.
                       
                       Folk dances are numerous in number and style, and vary according to the local tradition of the respective state, ethnic or geographic regions. Contemporary dances include refined and experimental fusions of classical, folk and Western forms. Dancing traditions of India have influence not only over the dances in the whole of South Asia, but on the dancing forms of South East Asia as well. Dances in Indian films are often noted for their idiosyncrasies, and hold a significant presence in popular culture of the Indian subcontinent.</p>
                       
