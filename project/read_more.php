                     <h2 align="justify"> Origin of Dance in India</h2>
                      <p align="justify"> Shiva as Nataraja is worshipped as the Lord of Dance in Hinduism.
                       In Hindu mythology, dance is believed to have been conceived by Brahma. Brahma inspired the sage Bharata Muni to write the Natya Shastra, a treatise on performing arts, from which a codified practice of dance and drama emerged. He used pathya (words) from the Rigveda, abhinaya (gestures) from the Yajurveda, geet (music) from the Samaveda and rasa (emotions) from the Atharvaveda to form the Natyaveda (body of knowledge about dance). The best-known of Hindu deities—Shiva, Kali and Krishna—are typically represented dancing. Shiva's cosmic dance, tandava, Kali's dance of creation and destruction and Krishna's dance with the gopikas (cow-herd girls)—Rasa Lila—are popular motifs in Hindu mythology.
                       In ancient India, there were no dedicated auditorium halls or theaters, and dance was usually a functional activity dedicated to worship, entertainment or leisure. Dancers usually performed in temples, on festive occasions and seasonal harvests. Dance was performed on a regular basis before deities as a form of worship. Even in modern India, deities are invoked through religious folk dance forms from ancient times.
                      </p>
                       
                     
                      <h2 align="justify"> Indian classical dance</h2>
                       
                      <p align="justify"> Classical dance in India has developed a type of dance-drama that is a form of a total theater. The dancer acts out a story almost exclusively through gestures. Most of the classical dances enact stories from Hindu mythology. Each form represents the culture and ethos of a particular region or a group of people.
                       
                       The criteria for being considered as classical is the style's adherence to the guidelines laid down in Natyashastra, which explains the Indian art of acting. The Sangeet Natak Akademi currently confers classical status on eight Indian classical dance styles: Bharatanatyam (Tamil Nadu), Kathak (North India), Kathakali (Kerala), Kuchipudi (Andhra Pradesh), Manipuri (Manipur), Mohiniyattam (Kerala), Odissi (Odisha), and Sattriya (Assam).
                       
                    </p>
                       
                   
                      <h2 align="justify"> Contemporary dance</h2>
                      <p align="justify"> Four women wearing saree in different dancing poses
                       Dance accompanied by Rabindra Sangeet, a music genre started by Rabindranath Tagore.
                       Contemporary dance in India encompasses a wide range of dance activities currently performed in India. It includes choreography for Indian cinema, modern Indian ballet and experiments with existing classical and folk forms of dance by various artists.
                      </p>
                       
                      <h2 align="justify"> Dance in films</h2>
                                            
                      <p align="justify"> Dance and song sequences have been an integral component of films across the country. With the introduction of sound to cinema in the film Alam Ara in 1931, choreographed dance sequences became ubiquitous in Hindi and other Indian films.</p>
                     <h2 align="justify"> Bollywood dance</h2>                        
                      <p align="justify"> Dance in early Hindi films was primarily modelled on classical Indian dance styles and particularly those of historic North Indian courtesans (tawaif), or folk dancers. Modern films often blend this earlier style with Western dance styles (MTV or in Broadway musicals), though it is not unusual to see western choreography and adapted classical dance numbers side by side in the same film.
                       </p>
                      
                      <h2 align="justify"> Dance education</h2>
                       
                      <p align="justify"> Traditionally, dance as a profession or a hobby was discouraged among upper classes and higher castes in India. It was looked upon as a lowly activity and women from reputed households were prohibited from practising any dance form. Hence, in forms like Bhavai and Gotipua, men would take up the roles of female characters.
                       Rukmini Devi Arundale revived the classical dance form Bharata Natyam and in its modern avatar, it became an acceptable subject of training for women. Shiamak Davar, a noted Indian choreographer, started the Shiamak Davar Institute for Performing Arts in 1985 and it has over 25,000 members.</p>
                       
                    
                       
                       
                       
