                  <p align="justify"  style="color:#000;">   Dances performed inside the sanctum of the temple according to the rituals were called Agama Nartanam. Natya Shastra classifies this type of dance form as margi, or the soul-liberating dance.
                        Dances performed in royal courts to the accompaniment of classical music were called Carnatakam. In the most essential sense, a Hindu deity is a revered royal guest in his temple/abode, to be offered the "sixteen hospitality" - among which are music and dance, pleasing to the senses. This was an intellectual art form.
                        
                        The term "classical" (Sanskrit: "Shastriya") was introduced by Sangeet Natak Akademi to denote the Natya Shastra-based performing art styles. The Classical dance form performed usually describes a story between the good and the evil. As the entire dance is presented in a dramatic manner nritta it enforces clean facial expressions and mudrƒÅ or hand gestures to narrate a story and to demonstrate certain concepts such as objects, weather, nature and emotions. Thus, the Classical Indian dance is also known as Natya . Even though the art of Natya includes nritta or dance proper, Natya has never been limited to dancing and includes singing and abhinaya (mime acting). These features are common to all of the Indian classical styles. In the margi form, Nritta is composed of karanas, while desi nritta consists mainly of adavus.
