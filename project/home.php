                    <p align="justify" style="color:#000;" >Folk dance in India is a term broadly used to describe all forms of folk and tribal dances in regions across India. While Indian classical dance is considered a higher form of art and was practiced in courts, temples and on special occasions, folk dance forms are practiced in groups in rural areas as an expression of their daily work and rituals. They are performed on religious or seasonal festivals. Some of the popular folk dances that are performed across villages and cities are Bhangra, Garba, Kalbelia and Bihu.</p>
