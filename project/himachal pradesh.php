                    <h2>Kinnauri Nati</h2>

<p align="justify" style="color:#000;">The beauty of hilly Himachal finds an expression in the languid and elegant movements that form a part of the marvelous Nati dance. The dance matches the gentleness of the hilly breeze and the rhythmic swaying of trees. The dance is mainly a mime but also incorporates some abstract but languid sequences. Important among the dances of Nati is 'Losar shona chuksom', which takes its name from Losai, or the New Year. The dance depicts all the activities involved in sowing the crop and reaping it.</p>
<h2 align="justify">Namgen</h2>

<p align="justify" style="color:#000;">The Namagen dance is performed in September to celebrate the autumnal hues. The costumes are largely woolen and richly studded ornaments of silver are worn by women. The most picturesque amongst these are dances of Gaddis. All regions of Himachal Pradesh have their own dances. Mostly men and women dance together, close to each other in the formation</p>
