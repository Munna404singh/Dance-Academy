               <h2> Namagen</h2>
<p align="justify" style="color:#000;">The autumnal hue is celebrated in the month of September in the Himachal and for the celebration of this festival the Namagen dance is performed. The most striking dance performance amongst these is the Gaddis. The costumes that are used in this dance are of woolens and women wear richly studded ornaments of silver. The dancing steps & rhythm of the dance is wonderfully mixed with each other. Drum plays an important part in this dance performance.</p>
<h2  style="width:500px;">Losar Shona Chuksam </h2>
<p align="justify"style="color:#000;" >The Losar Shona Chuksam is an agricultural festival dance performance. Losar refers to the New Year of the Tibetan people and it is performed by the Kinnauris in their typical style. The dancing steps depict all activities that are related with farming from sowing to reaping barley and phaphar (a local grain). The dance performance also includes innovative performed in the form of mime.</p>


