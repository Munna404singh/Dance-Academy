                    <h2 align="justify">Panthi</h2>

<p align="justify" style="color:#000;">The folk dance of the Satnami community of Chhattisgarh bears religious overtones. Performed on Maghi Purnima - the birth anniversary of their Guru(Saint) Guru Ghasidas, the dance is evolving still to include a variety of steps and patterns. The dancers dance around a jaitkhamb set up for the occasion, to the songs eulogizing their spiritual head. The songs also reflect the Nirvana philosophy, conveying the spirit of renunciation of their Guru and the teachings of saint poets like Kabir, Ramdas, Dadu, etc. Dancers with bent torsos and swinging arms continue to dance till carried away by their devotion. As the rhythm quickens, they indulge in acrobatics and even form human pyramids.</p>
