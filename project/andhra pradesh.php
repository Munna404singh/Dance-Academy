                  <h2> Burrakatha</h2>
                <p align="justify" style="color:#000;"> Burrakatha is the new name for the theatre known as Jangam Katha. The jangams were wandering Shiva worshippers.
Burra refers to the tambura, a musical instrument played by the main storyteller. The main performer narrates a story, plays music and dances to it. The co-performers plays drums and addresses him constantly and enrich certain events in the story with their short sentences.
Veeranatyam very old form of dance in the state of Andhra Pradesh that has a lot of religious significance attached to it; that is Veeranatyam. This fascinating kind of dance form is also known as Veerangam and Veerabhadra Nrityam. The term ‘Veera’ literally means brave. Thus as the name of the dance form suggests it is the dance of the brave.
The illustration of Veeranatyam is found in the Hindu Mythology. Once Lord Shiva’s wife, Sati Devi was humiliated at a function. This made the Shiva-the God of destruction, furious. Lord Shiva, outraged at the humiliation met by his consort, Lord Shiva picked out a relic out of His hair or ‘Jatajuta’, which created Veerabhadra. He is believed to have portrayed out His extreme anger by performing a vigorous dance; thus justifying the name Veeranatyam. It was the dance of ‘Pralayam’ or Destruction.
The angry destructive Shiva or the ‘Pralayankar’ in the ferocity of rage tarnished the ‘Dakshayagna Vatika’, the place where the function was held.
The Veerabhadriya (Veeramusti community, changed its name recently from Veeramusti to Veerabhadriya) which claims to be the descendant of Veerabhadra, performs this vigorous dance with instruments like Tambura, Soolam, Dolu, Tasha and Veeranam usually at Draksharamam in East Godavari District of Andhra Pradesh, which is believed to be Dakshavatika, the birthplace of Veerabhadra</p>
 <h2> Butta bommalu</h2>
<p align="justify" style="color:#000;">A typical folk dance form, popular in Tanuku of West Godavari District of Andhra Pradesh, Butta Bommalu which literally means basket toys are made of woodhusk, dry grass and cow dung. Each dancer wears a different mask over the head and shoulders enlarging the scope of the performer and dances to a nonverbal rhythm which adds colour to the movements.</p>

