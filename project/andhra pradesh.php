                 <h2 align="justify">kolattan</h2>
                    <p align="justify" style="color:#000;">Kolattam or "the stick dance", is one of the most popular dance narratives in Andhra Pradesh. It is also called as Kolannalu or Kolkolannalu. It's a rural art usually performed during village festivals. It is a combination of rhythmic movements, songs and music. The Kolatam group comprises dancers ranging from 8 to 40 where they are grouped in pairs. The sticks provides the main rhythm. The dancers are led by the leader and move about in two circles. The inner circle receive the strikes on their sticks from the artists in the outer circle that deliver them. Kolattam is also called Kolanna in the Prakasam district of Andhra Pradesh state.</p>
                 <h2 align="justify">Kuchipudi</h2>
