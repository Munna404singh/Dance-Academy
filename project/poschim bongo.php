                <h2>Chhau Dance</h2>
<p align="justify"  style="color:#000;">The Chhau dance of West Bengal is originated from Purulia district. It is included in the sophisticated dance performances of Bengal. The Chhau dance is a mask dance in which only male dancers can participate. In the performance of the Chhau, some of the characteristics of primitive ritualistic dance performance are noted. This can be also seen through its vigour, style and musical accompaniment mainly with the drum.Basically, it is a festival dance, performed on the occasion of the sun festival observed towards the end of the month of Chaitra as per Bengali Calendar. The festival is celebrated for about one-and-a-half months till before the starting of sowing season. This indicates its linkage with the social and economic life of the Bengal farmer & common man.</p>
<h2>Jatra</h2>
<p align="justify"  style="color:#000;">Jatra is a considered as a famous form of folk theatre from the eastern region of India. Jatra is the traditional theatre form of West Bengal. It is the enactment of a play. And performed with a cast and also comprises of music, dance, acting, singing and dramatic conflict in its performance. In earlier years, the religious values were well communicated with the help of Jatra to the masses. In short, it is used as the powerful medium of communication & awareness creation.</p>
                
