                    <h2 align="justify">Gambhira</h2>

<p align="justify"  style="color:#000;">The folk dance/theater of Gambhira originated among the Hindu community of Maldah in West Bengal. After Partition of India, Chapai Nawabganj in Rajshahi became the main center of Gambhira. With time, Gambhira has undergone many changes in terms of theme and style of its presentation. Muslims also became the custodian of the dance, and thereby it became an integral part of their culture. May be for that reason the dancer now wears the Lungi. Gambhira comprises a few characters with dialogues in an atmosphere of music, its themes now being contemporary social problems, fakeness and selfishness of people and so on.</p>

<h2 align="justify">Kalikapatadi</h2>

<p align="justify"  style="color:#000;">The main story of this Bengali dance form is 'how Shiva calms down angry Kali after killing Asura. It is more prevalent in Howrah. Before the coronation of Shiva on Neelpuja Day (Chaitra Sankranti), the performance of this dance is a must. The green leaves of water hyacinth is used to make the hair of Kali and the black ash of Ganja to decorate the body. Clay mask is used for Mahadeva. Palm leaves reddened with Alta is used as the tongue of Kali. Participants go on fast for the whole day. The dance is being performed for nearly five-hundred years.</p>

<h2 align="justify">Domni</h2>

<p align="justify"  style="color:#000;">Domni belongs to Maldah in West Bengal. A Domni performance starts with a Vandana[disambiguation needed] dedicated to God. Then the 'Mool Gayen' (Lead Character/Protagonist) and 'Chhokras' (Supporting Characters) offer devotional prayers. The dance performances of the Chhokras are called 'Nachari' or 'Lachari'. The main characters are the roles of husbands, wives, mothers, greedy moneylenders, peasant- girls and so on. The plays are composed taking extracts from small events of everyday life and are presented in a satirical manner. The musical instruments are Harmonium, Dholak, Kartal, Flute and so on. Domni groups are found in Maldah. With change on social life and popular taste/culture, this folk form is becoming extinct.
</p>
