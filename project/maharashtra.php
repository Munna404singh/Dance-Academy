                <h2>Dindi Dance</h2>
<p align="justify" style="color:#000;">In Maharashatra, religious devotional dances are called as Dindi. The verses & poems form the vital part of Dindi. The musicians for this dance comprise of a `Mridangam` player and a vocalist who give the dancers the necessary musical background. Mridangam is a kind of instrument that is used for generating music. This dance is usually performed on the Ekadashi day in the month of Kartik.
Dindi is a widely popular in Varkari people. This is a religious devotional dance that describes the playful attitude of Lord Krishna. Dindi is a small drum, like a `Tamate` used for musical purpose. Men and women folk in together perform the dance on the rhythmic music.</p>
<h2>Tamasha</h2>
<p align="justify" style="color:#000;">This is a form of theatre called Tamasha, which came into being in Maharashtra in the early 16th century. The history of the Tamasha of Maharashtra presents a picture, which is somewhat different from the other folk forms in Maharashtra.
The word Tamasha is a Persian of having meaning fun or entertainment. Some scholars even believe that this form of theatre has been strongly inspired by two forms of Sanskrit drama - the "Prahsana" and the "Bhana".
The interspersed poetry and narrative writing in Tamasha tells us of the existence of many dance and music styles such as the themes of the Ramayan and the Mahabharat. This is obvious but one can not find a direct bearing of early and medieval writing in it.</p>
                
                
