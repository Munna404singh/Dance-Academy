                    <h2 align="justify">Garba</h2>

<p align="justify" style="color:#000;">Garba is customarily performed by women, the dance involves circular patterns of movement and rhythmic clapping. It popularly performed during Navratri. The word comes from "garbha deep" which is translated as either light in the inner sanctum of the temple or lamp inside a perforated earthen pot (which is often used in the dance).</p>

<h2 align="justify">Raas</h2>

<p align="justify" style="color:#000;">Raas is an energetic, vibrant dance originating in the state of Gujarat. Often called the "stick dance" because it uses polished sticks or dandiya, it represents a mock-fight between Durga and Mahishasura, the mighty demon-king. It is nicknamed "The Sword Dance" because the dandiya represent the sword of Durga and are hit together. The combination of garba and raas has become very popular at the collegiate level in the United States. Garba-Raas competitions are increasing in number. Popular ones include Dandia Dhamaka, Raas Chaos, Garba With Attitude, Dandia on Fire and Maryland Masti among others.</p>

