<h2>Dandia-Ras</h2>
<p align="justify" style="color:#000;">The most popular Dandia-Ras is also known as the `stick` dance. An another form of dance that is also a feature of most welcomed festival, Navratri.
The word Ras in `Dandia-Ras` signifies Ras dance, which is, considered a form of Ras Leela. Ras Leela, which was an inseparable part of Lord Krishna`s childhood action he used to perform at Gokul and Vrindavan. The Ras is simple and is generally performed by a group of youthful people who move in typical style in measured steps around a circle, accompanied by a singing chorus and a host of musical instruments like the dhol, cymbals, zanz, shehnai (flute).
It is one of India`s most popular folk dances & is full of energy and excitement. It is considered that this dance form is from the Limbdi region of Gujarat (a state in Western India), over a period of time, the Dandiya Ras has imbibed an all parts of India. Like most folk dances of India, the Ras too is one of collective impulse and enthusiasm. It`s always performed in a group. In the ancient times, men essentially performed this folk dance. </p>

<h2>Bhavai Dance</h2>
<p align="justify" style="color:#000;">The Bhavai drama is a continuous performance lasting the whole night. In Bhavai many "veshas" are performed in the open, without any stage equipment. These veshas are usually episodes from the social life of the community in the countryside, focusing in satirical or farcical way with which certain sections are characterized- Banias, Bohras, wandering tribes, etc. Continuous playing on the Bhungal, a very long wind brass instrument, before and during the performance calls the villagers to the scene of the Bhavai. Women are not strictly allowed to take part in the Bhavai. Therefore, the male artists perform female roles, which makes the entire drama more joyful.</p>

