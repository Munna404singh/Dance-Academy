           <h2> Khuallam</h2>
<p align="justify" style="color:#000;">Khual, in the Mizo language, means guest, and lam stands for dancing. So, Khuallam is the dance of the guest. To have a place in paradise, one had to prove one's mettle either in war or in hunting or by being a man of distinction in society. To claim a distinguished place in society, one had to perform various ceremonies which included offering community feasts and dances. These ceremonies, taken together, were known as 'Khuangchawi'. While performing "Khuangchawi" one was obliged to invite relatives from nearby villages. The guests entered the arena of the "Khuangchawi" dancing Khuallam. The dance is normally performed by men dressed in 'Paundum' (traditional Mizo clothes with red and green stripes) to the accompaniment of a set of gongs known as Darbu.</p>
<h2>Chheih</h2>
<p align="justify" style="color:#000;">'Joie de vivre' would be the appropriate term to describe 'Chheih Lam', a dance that embodies the spirit of joy and exhilaration. 'Chheih Lam' is performed to the accompaniment of a song called ''Chheih hla'. The song is sung to the beats of a drum or bamboo tube or clapping of hands. People sqaut on the floor in a circle while a dancer stands in the middle reciting a song with various movements of limbs and body.</p>
