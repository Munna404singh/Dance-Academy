<h2 align="justify">Cheraw</h2>
<p align="justify" style="color:#000;">Cheraw is a very old traditional dance of the Mizos. It is believed that the dance had already existed way back in the 1st Century A.D., while the Mizos were still somewhere in the Yunan Province of China, before their migration into the Chin Hills in the 13th Century A.D., and eventually to the present Mizoram. Some of the tribes living in South East Asia have similar dances in one form or the other with different names.	 	</p>

