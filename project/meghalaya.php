<h2 align="justify">Bihu Dance</h2>
<p align="justify">Bihu is the most popular & colorful folk dance of Assam. The dance is performed in the Bihu festival, celebrated for the arrival of spring in the Assamese New Year. The people of Assam enjoy this festival a lot. This is extremely energetic, fast & eye-catching dance performance with the rhythmic exuberance of Bihu. Bihu dances are performed by young boys and girls characterized by brisk stepping, flinging and movements of hands, stylish footwork and swaying of hips represents youthful passion & reproductive urge.

Bihu dance is performed in the seedtime and also during the season of marriage. In fact, the festival had its roots in some earlier fertility cult. This preoccupation with fertility, both vegetal and human is reflected in the songs and dance with their overly erotic movements and expressions depicting the joys of spring and youth. </p>

<h2 align="justify">Nongkrem </h2>
<p align="justify" style="color:#000;">`Nongkrem` is the name of an important folkdance from the Meghalaya. The Khasis tribe of Meghalaya is related with its performance. They also celebrate the ripening of paddy for threshing by the way of dancing and singing.This dance is performed in the Nogkrem dance festival.
The Nongkrem dance festival of the Khasi tribe in India`s northeastern Meghalaya State is a multipurpose event. With the reason of this event, all tribes of this hilly region area people meet together from their scattered hamlets. They strongly believe in-group prayer or community gathering. Through this dance performance, they give thanks to the gods for a bountiful harvest. They also prey to nature to keep up the timely delivery of rain and ward off evil disasters. The dancing steps are very simple. Similarly, there is no any kind of agelimit to take part in the performance.</p>
