                <h2  style="width:500px;">Ka Shad Suk Mynsiem </h2>
                <p align="justify" style="color:#000;"> Ka Shad Suk Mynsiem is an annual spring dance that celebrates the harvesting and sowing seasons. This dance is performed to celebrate the agricultural cycles. The performers of the dance are girls and boys who wear colorful clothes and jewelry. However, only unmarried-virgin girls are allowed to perform this dance.</p>
  <h2  style="width:500px;" style="color:#000;">  Ka Pom-Blang Nongkrem</h2> <br />
<p align="justify">The Nongkrem dance is a part of the religious festival, Ka Pom-Blang Nongkrem. Unmarried girls decked with gorgeous attire, gold and silver ornaments, and yellow flowers, dance forward and backward forming a circle. Men also dance in a wide circle with a sword and a white hair whisk in their hands.</p>
  <h2>  Laho Dance</h2> 
  <p align="justify" style="color:#000;">Laho dance forms a part of the Behdienkhlam festival. Men and women dressed in colorful garments take active part in the dance form. This dance form is performed by a girl, linking arms with two boys on either side. It is remarkable that, instead of any musical instruments, a man with a natural talent in recitation, recites couplets during the dance performances.</p>
                
